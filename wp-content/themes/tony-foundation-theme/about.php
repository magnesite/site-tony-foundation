				<p>My name is Tony Laudadio, I am 34, and I have cancer. I was diagnosed on September 24, 2015. I was previously diagnosed with a different primary cancer at age 29, in July of 2013. My first bout was with renal cell carcinoma and a very large tumor. It caused me to lose my right kidney and forced me out of work for half a year. This time around it is brain cancer and completely unrelated to the first one. I thought I was well on my way to recovery and potentially ringing the bell (sort of a fun cancer free ritual they do at MD Anderson) when I unexpectedly had two seizures a week apart and found out about this new diagnosis.</p>

				<p>After my docs determined I had Oligodendroglia Grade 3 Brain cancer, I underwent a successful "awake craniotomy," which is a very specialized and rare surgery. It was about 8-10 hours long and I was awake for most of it. Following that, I underwent 30 days of brain radiation and 14 months of physical therapy and chemotherapy. Currently, my cancer is stable. But the fight we are facing is far from over and it will most likely be one that I battle for a long time.</p>

